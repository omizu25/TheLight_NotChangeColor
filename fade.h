#ifndef _FADE_H_	// このマクロ定義がされてなかったら
#define _FADE_H_	// ２重インクルード防止のマクロ定義

//==================================================
// 列挙型
//==================================================
typedef enum
{
	FADE_NONE = 0,	// 何もしていない状態
	FADE_OUT,		// フェードアウト(見えない)
	FADE_IN,		// フェードイン(見える)
	FADE_MAX
}FADE;

//==================================================
// プロトタイプ宣言
//==================================================
//--------------------------------------------------
// 初期化
//--------------------------------------------------
void InitFade(void);

//--------------------------------------------------
// 終了
//--------------------------------------------------
void UninitFade(void);

//--------------------------------------------------
// 更新
//--------------------------------------------------
void UpdateFade(void);

//--------------------------------------------------
// 描画
//--------------------------------------------------
void DrawFade(void);

//--------------------------------------------------
// 初期化
//--------------------------------------------------
void StartFadeOut(void);

//--------------------------------------------------
// 取得
// 返値  : FADE / 列挙型 状態
//--------------------------------------------------
FADE GetFade(void);

#endif // !_FADE_H_
