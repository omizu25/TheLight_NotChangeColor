#ifndef _MODE_H_	// このマクロが定義されてなかったら
#define _MODE_H_	// 2重インクルード防止のマクロ定義

//==================================================
// 列挙型
//==================================================
typedef enum
{
	MODE_TITLE = 0,	// タイトル
	MODE_TUTORIAL,	// チュートリアル
	MODE_GAME,		// ゲーム
	MODE_RESULT,	// リザルト
	MODE_MAX,
	MODE_NONE
}MODE;

//==================================================
// プロトタイプ宣言
//==================================================
//--------------------------------------------------
// 初期化
//--------------------------------------------------
void InitMode(void);

//--------------------------------------------------
// 終了
//--------------------------------------------------
void UninitMode(void);

//--------------------------------------------------
// 更新
//--------------------------------------------------
void UpdateMode(void);

//--------------------------------------------------
// 描画
//--------------------------------------------------
void DrawMode(void);

//--------------------------------------------------
// 設定
//--------------------------------------------------
void SetMode(void);

//--------------------------------------------------
// 取得
// 返値  : MODE / 列挙型 現在のモード
//--------------------------------------------------
MODE GetMode(void);

//--------------------------------------------------
// 変更
// 引数  : MODE modeNext / 列挙型 次のモード
//--------------------------------------------------
void ChangeMode(MODE modeNext);

#endif // !_MODE_H_
